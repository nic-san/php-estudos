    echo "\n";
}else{
    echo "a não é igual a b";
    echo "\n";
}   

if($a > $b){
    echo "a é maior que b";
    echo "\n";
}else{
    echo "a é menor que b";
    echo "\n";
}

if ($a >= $b){
    echo "a é maior ou igual a b";
    echo "\n";
}else{
    echo "a é menor ou igual a b";
    echo "\n";
}